#Membuat Program untuk Menyapa Pengunjung
print("Memasukkan Nama dan Jenis Kelamin")
nama_pengunjung = input("Masukkan nama anda: ")
jenis_kelamin = input("Masukkan jenis kelamin: ")
if jenis_kelamin == ('Perempuan'):
    print("Selamat Datang, Nyonya", nama_pengunjung, "!")
else:
    print("Selamat Datang, Tuan", nama_pengunjung, "!")
print()